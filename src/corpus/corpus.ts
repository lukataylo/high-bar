// Hand-authored cards span the style space deliberately; scraped cards are
// real sites run through the training-bench extractor (`pnpm scrape`, see
// bench/scrape/). Both feed the same deck.
export const CORPUS: InspirationCard[] = [...RAW_CORPUS, ...SCRAPED_CORPUS].map((c) => ({
  ...c,
  kind: "inspiration",
}));

// Order the first N cards as maximally-contrasting pairs so early swipes carry
// big signal (the deck shouldn't feel random at the start).
export function orderedCorpus(): InspirationCard[] {
  const rest = [...CORPUS];
  const ordered: InspirationCard[] = [];
  // Seed with a known strong opposition, then greedily alternate far pairs.
  const firstIdx = rest.findIndex((c) => c.id === "insp-corporate-saas");
  if (firstIdx >= 0) ordered.push(rest.splice(firstIdx, 1)[0]);
  while (rest.length) {
    const last = ordered[ordered.length - 1];
    let bestIdx = 0;
    let bestDist = -1;
    for (let i = 0; i < rest.length; i++) {
      const d = attrDistance(last.attrs, rest[i].attrs);
      if (d > bestDist) {
        bestDist = d;
        bestIdx = i;
      }
    }
    ordered.push(rest.splice(bestIdx, 1)[0]);
  }
  return ordered;
}

function attrDistance(a: TasteVector, b: TasteVector): number {
  let s = 0;
  for (const k in a) s += (a[k as DimensionKey] - b[k as DimensionKey]) ** 2;
  return Math.sqrt(s);
}
