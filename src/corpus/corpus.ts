// Compact vector builder: pass overrides, everything else defaults to 0.5.
function vec(partial: Partial<Record<DimensionKey, number>>): TasteVector {
  return {
    density: 0.5,
    radius: 0.5,
    saturation: 0.5,
    contrast: 0.5,
    mode: 0.5,
    type_class: 0.5,
    type_weight: 0.5,
    spacing_rhythm: 0.5,
    ornament: 0.5,
    gradients: 0.5,
    depth: 0.5,
    motion: 0.5,
    playfulness: 0.5,
    texture: 0.5,
    ...partial,
  };
}

